# Write a R program to add, multiply 
# and divide two vectors of integer type. 
# (vector length should be minimum 4)
x = c(10, 20, 30)
y = c(20, 10, 40)
print("Original Vectors:")
print(x)
print(y)
print("addition of two Vectors:")
z = x + y
print(z)
print("multiply of two Vectors:")
z = x * y
print(z)
print("division of two Vectors:")
z = x / y
print(z)